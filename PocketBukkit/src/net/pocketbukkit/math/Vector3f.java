package net.pocketbukkit.math;

public class Vector3f {
public static float vectorx;
public static float vectory;
public static float vectorz;
	
	public static void vector3f() {
		vectorx = 0.0f;
		vectory = 0.0f;
		vectorz = 0.0f;
	}
	
	public static void vector3f(float x, float y, float z) {
		vectorx = x;
		vectory = y;
		vectorz = z;
	}
}
